function ExampleCard({ example }: { example: Example }) {
  const [isExpanded, setIsExpanded] = useState(false);
  const [copied, setCopied] = useState(false);

  const copyPrompt = () => {
    navigator.clipboard.writeText(example.prompt);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="bg-white rounded-lg shadow-sm border border-gray-100 p-4 hover:shadow-md transition-all duration-300">
      
      {/* العنوان والتقييم */}
      <div className="flex items-start justify-between mb-2">
        <h3 className="text-base font-bold text-gray-800">{example.title}</h3>
        <div className="flex gap-0.5">
          {[...Array(example.rating)].map((_, i) => (
            <Star key={i} className="w-3 h-3 fill-amber-400 text-amber-400" />
          ))}
        </div>
      </div>

      {/* معلومات الخبير */}
      <div className="flex items-center gap-2 mb-2">
        <div className="w-6 h-6 rounded-full bg-purple-100 flex items-center justify-center flex-shrink-0">
          <User className="w-3 h-3 text-purple-600" />
        </div>
        <div className="flex-1 min-w-0">
          <p className="text-xs font-semibold text-gray-700 truncate">{example.expert}</p>
          <p className="text-xs text-gray-500 line-clamp-1">{example.description}</p>
        </div>
      </div>

      {/* الفئة */}
      <div className="inline-block px-2 py-0.5 bg-green-50 text-green-700 text-xs font-medium rounded-full mb-2">
        {example.category}
      </div>

      {/* زر عرض التفاصيل */}
      <button
        onClick={() => setIsExpanded(!isExpanded)}
        className="w-full flex items-center justify-between text-xs text-gray-600 hover:text-gray-800 mb-2 transition-colors"
      >
        <span>عرض التفاصيل</span>
        <ChevronDown 
          className={`w-3 h-3 transition-transform duration-300 ${isExpanded ? 'rotate-180' : ''}`} 
        />
      </button>

      {/* البرومبت الكامل */}
      {isExpanded && (
        <div className="bg-gray-50 rounded-lg p-3 mb-2">
          <p className="text-xs text-gray-700 leading-relaxed mb-2">
            {example.prompt}
          </p>
          <div className="flex gap-1 flex-wrap">
            <span className="px-1.5 py-0.5 bg-white text-xs text-gray-600 rounded border border-gray-200">
              النمط: {example.style}
            </span>
            <span className="px-1.5 py-0.5 bg-white text-xs text-gray-600 rounded border border-gray-200">
              الجودة: {example.quality}
            </span>
            {example.useCase && (
              <span className="px-1.5 py-0.5 bg-white text-xs text-gray-600 rounded border border-gray-200">
                الاستخدام: {example.useCase}
              </span>
            )}
          </div>
        </div>
      )}

      {/* زر النسخ */}
      <button
        onClick={copyPrompt}
        className="w-full bg-gradient-to-r from-green-500 to-emerald-600 hover:from-green-600 hover:to-emerald-700 text-white rounded-lg py-2 px-3 flex items-center justify-center gap-1.5 text-sm font-medium transition-all shadow-sm hover:shadow-md active:scale-[0.98]"
      >
        <Copy className="w-3.5 h-3.5" />
        {copied ? '✓ تم النسخ' : 'نسخ البرومبت'}
      </button>
    </div>
  );
}

